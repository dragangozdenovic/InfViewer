package infviewer.workspace.controller;

import java.awt.event.ActionEvent;

import javax.swing.AbstractAction;

import infviewer.tree.model.Table;

/**
 * Nasledjuje {@link AbstractAction}. Sadrzi metode koje se koriste pri promovisanju deteta u roditelja, 
 * odnosno depromovisanju roditelja u red potomaka.
 * @author DrGoz
 *
 */
public class WorkspaceAction extends AbstractAction 
{   
   private static final long serialVersionUID = -6790059792119705052L;
   protected Table table;
   
   public WorkspaceAction(Table table)
   {
	   this.table = table;
   }
   
   public void doAction() { }

	@Override
	public void actionPerformed(ActionEvent e) {
		this.doAction();
	}
	
	public Table getTable()
	{
		return this.table;
	}
}